#include "rt.h"

double	**m4_mult_m4(double **m1, double **m2)
{
	double	**m3;
	int		i;
	int		j;

	m3 = m4_allocate();
	i = 0;
	while (i < 4)
	{
		j = 0;
		while (j < 4)
		{
			m3[i][j] = m1[i][0] * m2[0][j]
			+ m1[i][1] * m2[1][j]
			+ m1[i][2] * m2[2][j]
			+ m1[i][3] * m2[3][j];
			j++;
		}
		i++;
	}
	return (m3);
}
