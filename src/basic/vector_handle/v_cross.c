#include "rt.h"

t_3d_double	v_cross(t_3d_double a, t_3d_double b)
{
	t_3d_double	n;

	n.x = (a.y * b.z) - (a.z * b.y);
	n.y = (a.z * b.x) - (a.x * b.z);
	n.z = (a.x * b.y) - (a.y * b.x);
	return (n);
}
