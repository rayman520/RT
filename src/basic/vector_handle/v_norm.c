# include "rt.h"

t_3d_double	v_norm(t_3d_double v)
{
	double n;
	t_3d_double nv;

	n = v_len(v);
	nv.x = v.x / n;
	nv.y = v.y / n;
	nv.z = v.z / n;
	return (nv);
}
